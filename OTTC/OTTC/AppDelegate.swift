@UIApplicationMain
class AppDelegate: UIResponder, UIApplicationDelegate {

    var window: UIWindow?
    
    func application(_ app: UIApplication, open url: URL, options: [UIApplicationOpenURLOptionsKey : Any] = [:]) -> Bool {
        
        let urlString = "\(url)"
        
        if urlString == "OTTC://start"
        {
            NotificationCenter.default.post(name: Notification.Name("updateControls"), object: self, userInfo:["message":"start", "boby":"trip"] )
        }
        
        return true
    }
    
    func getOneSignalAppId() -> String {
        
        if let fileUrl = Bundle.main.url(forResource: "Config", withExtension: "plist"),
            let data = try? Data(contentsOf: fileUrl) {
            if let result = try? PropertyListSerialization.propertyList(from: data, options: [], format: nil) as! NSDictionary {
                return result.object(forKey: "OneSignalAppId") as! String
            }
        }
        
        return ""
    }
    
    func application(_ application: UIApplication, didFinishLaunchingWithOptions launchOptions: [UIApplicationLaunchOptionsKey : Any]? = nil) -> Bool {
        
        OneSignal.initWithLaunchOptions(launchOptions, appId: getOneSignalAppId())
        
        let eventChannel = EventChannel(applicationGroupIdentifier: "group.scipopulis.OTTC", eventChannel:"widget")
        eventChannel.subscribe()
        
        return true
    }

